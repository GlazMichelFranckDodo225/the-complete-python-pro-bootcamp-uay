# Rules and conventions for naming variables in Python

# There are several rules and conventions for naming variables.
# Here are the main ones:
# 1. **Allowed Characters**: Variable names can contain letters (a-z, A-Z), digits (0-9),
# and underscores (_). However, they cannot begin with a digit.
# 2. **No Reserved Words**: Variable names must not be Python's reserved words (such as
# `if`, `else`, `while`, `for`, `class`, `def`, etc.).
# 3. **Case Sensitivity**: Variable names are case-sensitive. For example, `myVariable`,
# `MyVariable`, and `MYVARIABLE` are considered three different variables.
# 4. **Length:** There is no strict limit on the length of variable names, but it is
# recommended to choose meaningful and concise names to improve code readability.
# 5. **Naming Conventions:**
# - Variable names are generally written in lowercase, with words separated by
# underscores (e.g., `my_variable`).
# - For constants, it is common to use uppercase letters with underscores
# (e.g., `MY_CONSTANT`).
# 6. **Use of Special Characters:** Avoid using special characters (such as @, #, $, etc.)
# in variable names.
#
# By following these rules, you can create variable names that are clear, readable,
# and conform to Python conventions.